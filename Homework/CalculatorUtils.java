package Homework;

public class CalculatorUtils {

    public static int CalculateAverage(int first, int second) {
        return (first + second) / 2;
    }

    public static int CalculateFemaleAverage(int first, int second) {
        return (first / 2 + second * 2) / 2;
    }

    public static int CalculateAverage(int first, int second, int third) {
        return (first + second + third) / 3;
    }
}
